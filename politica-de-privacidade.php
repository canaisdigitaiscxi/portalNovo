                        <p><b>Beacons:</b> São pequenos dispositivos que utilizam tecnologia <i>Bluetooth Low Energy</i> (BLE), que emitem sinais que podem ser captados por recursos tecnológicos, possibilitando a interação entre o <b>USUÁRIO</b> e o recurso tecnológico. Os <b>Web Beacons</b> são programações que tem como finalidade monitorar a navegação de um <b>USUÁRIO</b> em uma página na Internet.</p>

                        <p><b><i>CLOUD COMPUTING:</i></b> Ou computação em nuvem, é tecnologia de virtualização de serviços construída a partir da interligação de mais de um servidor por meio de uma rede de informação comum (p.ex. a Internet), com objetivo de reduzir custos e aumentar a disponibilidade dos serviços sustentados.</p>

                        <p><b><i>Cookies:</i></b> Arquivos enviados pelo servidor da <b>Plataforma</b> para o computador dos <b>USUÁRIOS</b>, com a finalidade de identificar o computador e obter dados de acesso, como páginas navegadas ou links acessados, permitindo, desta forma, personalizar a navegação dos <b>USUÁRIOS</b> na <b>Plataforma</b>, de acordo com o seu perfil.</p>

